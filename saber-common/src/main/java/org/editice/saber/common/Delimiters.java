package org.editice.saber.common;

/**
 * 常用的分隔符
 */
public interface Delimiters {

    /**
     * 井号
     */
    String POUND = "#";

	/**
	 * 空格
     */
    String BLANK=" ";

    /**
     * 冒号
     */
    String COLON = ":";

    /**
     * 下划线
     */
    String ULINE = "_";

    /**
     * 逗号
     */
    String COMMA = ",";

    /**
     * 分号
     */
    String SEMI = ";";

    /**
     * 破折号
     */
    String DASH = "-";

    /**
     * 竖线
     */
    String VLINE = "|";

    /**
     * 等号
     */
    String EQUAL = "=";

    /**
     * 与号
     */
    String AND = "&";

    /**
     * 问号
     */
    String ASK = "?";

    /**
     * 定位符
     */
    String AT = "@";

    /**
     * 换行符
     */
    String BR = "\n";

    /**
     * 上破折号
     */
    String UP = "^";

    String POINT = ".";

}
